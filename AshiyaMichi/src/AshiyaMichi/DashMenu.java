    private void varRemoveItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_varRemoveItemActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) varTable.getModel();
        int RemoveItem = varTable.getSelectedRow();
        if (RemoveItem >= 0) {
            model.removeRow(RemoveItem);
            JOptionPane.showMessageDialog(null,"Successfully Remove Item","Remove",JOptionPane.PLAIN_MESSAGE);
        }
        ItemCost();
        if (varBuy.getSelectedItem().equals("Cash")) {
            JOptionPane.showMessageDialog(null, "You remove the item","Remove",JOptionPane.PLAIN_MESSAGE);
        } else {
            varChange.setText("");
            varCash.setText("");
        }
    }//GEN-LAST:event_varRemoveItemActionPerformed

